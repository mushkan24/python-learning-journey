# Day 1 - Variables & Data Types Practice

# Assigning variables
name = "Mushkan"
age = 22
height = 5.4
is_student = True

# Printing variables
print("Name:", name)
print("Age:", age)
print("Height:", height)
print("Is Student:", is_student)

# Checking data types
print(type(name))     # str
print(type(age))      # int
print(type(height))   # float
print(type(is_student)) # bool

